    public void setEnemyFields(boolean value) {
        for (int i = 0; i < 10; i++) {
            for (int j = 0; j < 10; j++) {
                MatrizInimigo[i][j].setEnabled(value);
            }
        }
    }

    public void setFields(boolean value) {
        for (int i = 0; i < 10; i++) {
            for (int j = 0; j < 10; j++) {
                Matriz[i][j].setEnabled(value);
            }
        }
    }

    public void setFieldValue(int linha, int coluna, Color color) {
        //Matriz[linha][coluna].setText(Matriz[linha][coluna].tipo);
        Matriz[linha][coluna].setBackground(color);
        //Matriz[linha][coluna].setDisabledIcon(Matriz[linha][coluna].getIcon());
        Matriz[linha][coluna].setText("<html><font color = red>" + Matriz[linha][coluna].tipo + "</font></html>");
    }

    public Tile getTile(int linha, int coluna) {
        return Matriz[coluna][linha];
    }

    public boolean getTileOccupied(int linha, int coluna) {
        if (!Matriz[coluna][linha].tipo.equals("A")) {
            return true;
        } else {
            return false;
        }

    }

