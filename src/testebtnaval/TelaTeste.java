    private void jButtonEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEnviarActionPerformed
        if (!jTextFieldTexto.getText().isEmpty()) {
            try {
                servidor.sendMessage(this.client.GetName() + ": " + jTextFieldTexto.getText());
                jTextFieldTexto.setText("");
                jTextFieldTexto.grabFocus();
            } catch (RemoteException ex) {
                Logger.getLogger(TelaTeste.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButtonEnviarActionPerformed

    private void jButtonGiveUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGiveUpActionPerformed
        int dialogResult = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja desistir?", "Atenção!", JOptionPane.YES_NO_OPTION);
        if (dialogResult == JOptionPane.YES_OPTION) {
            try {
                servidor.GiveUp(client);
            } catch (RemoteException ex) {
                Logger.getLogger(TelaTeste.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButtonGiveUpActionPerformed

